def parse_data(filename, content):
    """解析数据文件"""
    data = []
    lines = content.strip().split('\n')
    count = 0
    for line in lines[0:]:  # 跳过第一行标题
        count += 1
        data.append((int(count*100),float(line)))
    return sorted(data, key=lambda x: x[0])  # 按数据量排序

